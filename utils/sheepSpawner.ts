/**
 * Check if a coordinate is within green grass based on Y-range lookup
 */
export const isPointInGrassArea = (
  x: number,
  y: number,
  platformWidth: number,
  platformHeight: number
): boolean => {
  const imageWidth = 2485;
  const imageHeight = 1893;

  const imageX = Math.round((x / platformWidth) * imageWidth);
  const imageY = Math.round((y / platformHeight) * imageHeight);

  if (imageX < 0 || imageX >= imageWidth || imageY < 0 || imageY >= imageHeight) {
    return false;
  }

  const range = GREEN_PIXEL_RANGES[imageX.toString()];
  if (!range) return false;

  const [minY, maxY] = range;
  return imageY >= minY && imageY <= maxY;
};

/**
 * Generate a random position within the diamond grass block using rejection sampling
 */
export const getRandomPositionInDiamond = (
  platformWidth: number,
  platformHeight: number,
  sheepSize: number = 60
): { x: number; y: number } => {
  let x = 0, y = 0;
  let attempts = 0;
  const maxAttempts = 100;

  do {
    x = Math.random() * platformWidth;
    y = Math.random() * platformHeight;
    attempts++;
  } while (
    !isPointInGrassArea(x, y, platformWidth, platformHeight) &&
    attempts < maxAttempts
  );

  x = Math.max(sheepSize / 2, Math.min(platformWidth - sheepSize / 2, x));
  y = Math.max(sheepSize / 2, Math.min(platformHeight - sheepSize / 2, y));

  return { x, y };
};
